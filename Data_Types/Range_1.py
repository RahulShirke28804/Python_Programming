'''
    This code shows how the range() function works in Python. 
    It creates a range of numbers, prints the range object itself, 
    and displays its data type. It also demonstrates a range with 
    a custom start, stop, and step value.
'''

data = range(7)

print(data)
print(type(data))

test = range(3,9,2)

print(test)
print(type(test))