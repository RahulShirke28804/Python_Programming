'''
    This program demonstrates how to check the data type of different variables 
    like int, float, str, bool, and complex.
'''


Age = 35            # int 
Marks = 90.89       # float
City = "Pune"       # str
Food = False        # bool
complex = 7+9j      # complex

print(type(Age))
print(type(Marks))
print(type(City))
print(type(Food))
print(type(complex))
